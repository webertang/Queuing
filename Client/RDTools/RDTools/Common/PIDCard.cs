        public bool GetPIDInfo(string pid)
        {
            if(pid.Length == 15)
            {
                try
                {
                    pid = PID15To18(pid);
                }
                catch
                {
                    return false;
                }
            }
            pid = pid.ToLower();
            
            double iSum = 0;
            System.Text.RegularExpressions.Regex rg = new System.Text.RegularExpressions.Regex(@"^\d{17}(\d|x)$");
            System.Text.RegularExpressions.Match mc = rg.Match(pid);
            if (!mc.Success)
            {
                return false;
            }
           
            pid = pid.Replace("x", "a");
            try
            {
                Area = AreaCode[pid.Substring(0, 6)].ToString();
            }
            catch
            {
                return false; //非法地区
            }
            
            try
            {
                Birthday = DateTime.Parse(pid.Substring(6, 4) + "-" + pid.Substring(10, 2) + "-" + pid.Substring(12, 2));
            }
            catch
            {
                return false; //"非法生日
            }
            for (int i = 17; i >= 0; i--)
            {
                iSum += (System.Math.Pow(2, i) % 11) * int.Parse(pid[17 - i].ToString(), System.Globalization.NumberStyles.HexNumber);

            }
            if (iSum % 11 != 1)
                return false; //非法证号

            try
            {
                Sex = int.Parse(pid.Substring(16, 1)) % 2 == 1 ? "男" : "女";
            }
            catch
            {
                return false; //性别非法;
            }

            return true;

        }

        private string PID15To18(string pid)
        {
            int iS = 0;

            //加权因子常数 
            int[] iW = new int[] { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2 };
            //校验码常数 
            string LastCode = "10X98765432";
            //新身份证号 
            string _PIDNew;

            _PIDNew = pid.Substring(0, 6);
            //填在第6位及第7位上填上‘1’，‘9’两个数字 
            _PIDNew += "19";

            _PIDNew += pid.Substring(6, 9);

            //进行加权求和 
            for (int i = 0; i < 17; i++)
            {
                iS += int.Parse(_PIDNew.Substring(i, 1)) * iW[i];
            }

            //取模运算，得到模值 
            int iY = iS % 11;
            //从LastCode中取得以模为索引号的值，加到身份证的最后一位，即为新身份证号。 
            _PIDNew += LastCode.Substring(iY, 1);

            return _PIDNew;
        } 
